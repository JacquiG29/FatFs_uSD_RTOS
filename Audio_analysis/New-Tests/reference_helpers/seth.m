function seth
%SETH  Set the current axes font size to 14 (plotting helper).
%   Reconstructed stub for calcT60_bands.m (used only when plotdecays=1).
%   The original name likely meant "set height/handle"; here it just applies
%   a consistent font size like sg14, which is all that context requires.
    set(gca, 'FontSize', 14);
end
